volatile uint32_t timer_ms = 0; // Define in main.c

int main(void)
{
    HAL_Init();
    SystemClock_Config();
    GPIO_Init();

    uint32_t last_time = 0;
    while (1)
    {
        if (timer_ms - last_time >= 300) // 300 ms delay
        {
            last_time = timer_ms;
            HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_8); // Toggle PC8
        }
    }
}

